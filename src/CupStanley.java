public class CupStanley {
    private String colorCup;
    private Float sizeCup;
    private String materialCup;

    public String getColorCup() {
        return colorCup;
    };

    public void setColorCup(String colorCup) {
        this.colorCup = colorCup;
    };

    public Float getSizeCup() {
        return sizeCup;
    };

    public void setSizeCup(Float sizeCup) {
        this.sizeCup = sizeCup;
    };

    public String getMaterialCup() {
        return materialCup;
    };

    public void setMaterialCup(String materialCup) {
        this.materialCup = materialCup;
    };

}


